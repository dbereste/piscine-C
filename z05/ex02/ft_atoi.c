int	ft_atoi(char *str)
{
	int n;
	int nb;
	int min;

	n = 0;
	nb = 0;
	min = 1;
	if (*str == '-')
	{
		min = min * -1;
		str++;
	}
	while (str[n] != '\0')
	{
		if (str[n] >= 48 && str[n] <= 57)
			nb = nb * 10 + (str[n] - '0');
		n++;
	}
	return (nb * min);
}
